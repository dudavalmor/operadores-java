package aulaOperadores;

public class OperadoresStrings {

	public static void main(String[] args) {
		String texto01 = "Bom";
		String texto02 = "Dia";
		String texto03 = " ";
		
		System.out.println("Concatenar texto01 e texto02: " + texto01 + texto02);
		System.out.println("\nConcatenar texto02 e texto01: " + texto02 + texto01);
		System.out.println("\nConcatenat texto01, texto03 e texto02: " + texto01 + texto03 + texto02);

	}

}
