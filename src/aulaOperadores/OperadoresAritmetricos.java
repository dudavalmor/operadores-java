package aulaOperadores;

public class OperadoresAritmetricos {

	public static void main(String[] args) {
		int x = 6;
		int y = 2;
		int z = 8;
		int w = -7;
		
		System.out.println("Soma (6+2): " + (x + y));
		System.out.println("\nSoma com numero negativo (6 + (-7)): " + (x + w));
		System.out.println("\nSubtração (6-2): " + (x - y));
		System.out.println("\nMultiplicação (6*2): " + (x * y));
		System.out.println("\nDivisão (6/2): " + (x / y));
		System.out.println("\nMódulo (resto da divisão): " + (x % 2));
		System.out.println("\nMódulo (resto da divisão): " + (z % 2));

	}

}
